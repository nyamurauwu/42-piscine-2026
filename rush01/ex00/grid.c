void	inicialize_grid(int grid[4][4])
{
	int	i;
	int	j;

	i = 0;
	while (i <= 3)
	{
		j = 0;
		while (j <= 3)
		{
			grid[i][j] = 0;
			j++;
		}
		i++;
	}
}
